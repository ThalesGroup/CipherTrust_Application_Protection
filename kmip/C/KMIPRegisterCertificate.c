I_KS_Result KMIPRegisterCertificate(I_O_Session *handle_p, char *name)
{
	I_KO_AttributeList attrList = NULL;
	I_KS_AttributeNameList* attrNameList_p = NULL;
	I_KS_Object managedObject;
	I_KS_Object* object_p = NULL;
	I_KS_GetRequest getRequest;
	I_KS_Result result;

	result = I_KC_CreateAttributeList(&attrList, NULL);
	if (result.status == I_KT_ResultStatus_Success)
	{
    		do
		{
			addName(attrList, name, NULL);
                        addCryptographicUsageMask(attrList);
			printAttrList(attrList, KMIP_REQUEST);

			managedObject.objectType_t = I_KT_ObjectType_Certificate;

			managedObject.object_u.certificate_s.certFormat 			= I_KT_CertificateFormat_X509;
			managedObject.object_u.certificate_s.keyBytes_s.byteString_p 	= testCertificate;
			managedObject.object_u.certificate_s.keyBytes_s.byteStringLen	= sizeof(testCertificate)/sizeof(testCertificate[0]);

			result = I_KC_Register(*handle_p, attrList, &managedObject);
			if (result.status != I_KT_ResultStatus_Success)
			{
				printf("I_KC_Register failed Status:%s Reason:%s\n",  I_KC_GetResultStatusString(result), I_KC_GetResultReasonString(result));
				break;
			}

			printAttrList(attrList, KMIP_RESPONSE);

			result = I_KC_GetAttributesList(*handle_p, attrList, &attrNameList_p);
			if (result.status != I_KT_ResultStatus_Success)
			{
				printf("I_KC_GetAttributeList failed Status:%s Reason:%s\n", I_KC_GetResultStatusString(result), I_KC_GetResultReasonString(result));
				break;
			}
			if(attrNameList_p != NULL) I_KC_FreeAttributeNameList(attrNameList_p);

			result = I_KC_Destroy(*handle_p, attrList);
			if (result.status != I_KT_ResultStatus_Success)
			{
				printf("I_KC_Destroy failed Status:%s Reason:%s\n",  I_KC_GetResultStatusString(result), I_KC_GetResultReasonString(result));
		                break;
 			}

		} while (0);
	}

	if (attrList != NULL)
	{
		I_KS_Result  result2 = I_KC_DeleteAttributeList(attrList);
		if (result2.status != I_KT_ResultStatus_Success) printf("I_KC_DeleteAttributeList failed Status:%s Reason:%s\n",  I_KC_GetResultStatusString(result2), I_KC_GetResultReasonString(result2));
	}

	return result;
}

int main(int argc, char **argv)
{
	I_O_Session sess;
	char *path, *certname = NULL;
	int argp;
	I_T_RETURN rc;
	I_KS_Result result;

	if (argc < 2)
		usage(); // exit

	argp = 1;
	path = argv[argp++];

	if (argc >= 3)
		certname = argv[argp];

	rc = I_C_Initialize(I_T_Init_File, path);
	if (rc != I_E_OK)
	{
		fprintf(stderr, "I_C_Initialize error: %s\n",  I_C_GetErrorString(rc));
		return rc;
	}

	rc = I_C_OpenSession(&sess, I_T_Auth_NoPassword, NULL, NULL);
	if (rc != I_E_OK)
	{
		fprintf(stderr, "I_C_OpenSession error: %s\n", I_C_GetErrorString(rc));
		I_C_Fini();
		return rc;
	}

	result = KMIPRegisterCertificate(&sess, certname);
	if (result.status != I_KT_ResultStatus_Success) 
		printf("RegisterCertificate failed Status:%s Reason:%s\n", I_KC_GetResultStatusString(result), I_KC_GetResultReasonString(result));
	else
		printf("RegisterCertificate Successful\n");

	I_C_CloseSession(sess);
	I_C_Fini();

	return rc;
}

